    private void butaoCriarEditorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_butaoCriarEditorMouseClicked

        if(nomeEditor.getText().length() < 3){
            new DialogoMsg("Nome do Editor precisa ter ao menos 3 caracteres");
            return;
        }
        Pattern pattern = Pattern.compile("\\d+.\\d+");

        if(!pattern.matcher(salarioEditor.getText()).matches()){
            new DialogoMsg("Salario do editor invalido");
            return;
        }
        try{
            Editor editor = new Editor();
            editor.setNome(nomeEditor.getText());
            editor.setSalario(Float.parseFloat(salarioEditor.getText()));
            if(CreateEditorController.add(editor)){
                table();
            }else{
                new DialogoMsg("N達o foi possivel Criar o editor");
                return;
            }
        }catch (Exception e){
            new DialogoMsg("Salario do editor invalido");
            return;
        }





    }//GEN-LAST:event_butaoCriarEditorMouseClicked

    private void excluirEditorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_excluirEditorMouseClicked
        try {
            Editor editor = GetEditorController.get( (UUID) listaEditores.getValueAt(listaEditores.getSelectedRow(), 0));
            if (editor != null) {
                if (DeleteEditorController.delete(editor)) {
                    table();
                } else {
                    new DialogoMsg("N達o foi possivel excluir o editor!");
                }
            } else {
                new DialogoMsg("Editor n達o encontrado!");
            }
        }catch (Exception e){
            new DialogoMsg("Nenhum Editor selecionado!");
        }
    }//GEN-LAST:event_excluirEditorMouseClicked

    private void logarEditorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_logarEditorMouseClicked
        try {
            Editor editor = GetEditorController.get((UUID) listaEditores.getValueAt(listaEditores.getSelectedRow(), 0));
            if (editor != null) {
                new PageEditor(editor).setVisible(true);
                this.setVisible(false);

            } else {
                new DialogoMsg("Editor n達o encontrado!");
            }
        }catch (Exception e){
            new DialogoMsg("Nenhum Editor selecionado!");
        }
    }//GEN-LAST:event_logarEditorMouseClicked

