    private void aprovarMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aprovarMateriaActionPerformed
        noticia.setAprovada(true);
        noticia.setAprovadoPor(chefe.getId());
        try {
            UpdateNoticiaController.update(noticia);
            new DialogoMsg("Noticia aprovada com sucesso");
        }catch (Exception e){
            new DialogoMsg(e.toString());
        }
        out();
    }//GEN-LAST:event_aprovarMateriaActionPerformed

    private void reprovarMateriaActionPerformed(java.awt.event.ActionEvent evt) {                                                
        if (comentarioReprovar.getText().length() <10){
            new DialogoMsg("Comentario para reprovar deve ter ao menos 10 caracteres!");
        }else{
            noticia.setEditada(false);
            noticia.setRevisada(false);
            noticia.setRevisaoCompleta(false);
            noticia.setAprovada(false);
            noticia.setCriada(false);
            noticia.setMotivoReprovada(comentarioReprovar.getText());
            try {
                UpdateNoticiaController.update(noticia);
                new DialogoMsg("Noticia reprovada com sucesso");
            }catch (Exception e){
                new DialogoMsg(e.toString());
            }
        }
        out();
    }
    public void out(){
        this.setVisible(false);
        new PageChefeRedacao(chefe).setVisible(true);
    }


