    private void imprimirActionPerformed(java.awt.event.ActionEvent evt) throws IOException {//GEN-FIRST:event_imprimirActionPerformed

        noticiaImprimir ="Titulo: "+ noticiaAtual.getTitulo();
        noticiaImprimir += "\nAutor: " + GetJornalistaController.get( noticiaAtual.getAuthor()).getNome();
        noticiaImprimir += "\nCategoria: " + GetCategoriaController.get( noticiaAtual.getCategoria()).getNome();
        noticiaImprimir += "\nData: " + noticiaAtual.getData();
        noticiaImprimir += "\nConteudo: \n " + noticiaAtual.getConteudo();
        noticiaImprimir+= "\n\n\n";
        noticiaImprimir+= "Anuncios: ";
        noticiaAtual.getAnuncios().forEach(anun -> {

            Date date = new Date();
            Anuncio an = GetAnuncioController.get(anun);

            String[] data = an.getFimVisualizacao().split("/");
            if(date.getYear() <= Integer.getInteger(data[2])){
                if(date.getMonth() <= Integer.getInteger(data[1])){
                    if(date.getDay() <= Integer.getInteger(data[0])){
                        noticiaImprimir += "\n\n" + an.getConteudo();
                        an.setExemplaresEntregues(an.getExemplaresEntregues()+1);
                    }
                }
            }
        });
        FileWriter wNoticias = new FileWriter("noticia.txt");
        wNoticias.write(noticiaImprimir);
        wNoticias.close();
        new DialogoMsg("Imprimido com sucesso!");

    }//GEN-LAST:event_imprimirActionPerformed
    public void setDadosIniciais(){
        titulo.setText(noticiaAtual.getTitulo());
        conteudo.setText(noticiaAtual.getConteudo());
        try {
            autor.setText(GetJornalistaController.get(noticiaAtual.getAtribuidoA()).getNome());
            categoria.setText(GetCategoriaController.get(noticiaAtual.getCategoria()).getNome());
            data.setText(noticiaAtual.getData());

            noticiaAtual.getAnuncios().forEach(anun -> {
                Date date = new Date();

                Anuncio an = GetAnuncioController.get(anun);
                String[] data = an.getFimVisualizacao().split("/");
                if(date.getYear() <= Integer.getInteger(data[2])){
                    if(date.getMonth() <= Integer.getInteger(data[1])){
                        if(date.getDay() <= Integer.getInteger(data[0])){
                            anuncio.setText(anuncio.getText() + "\n\n" + an.getConteudo());
                        }
                    }
                }
            });
        }catch (Exception e){
            new DialogoMsg("NÃ£o foi possivel encontrar os dados da noticia!");
        }
    }

