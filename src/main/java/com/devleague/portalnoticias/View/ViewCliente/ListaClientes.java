    private void butaoCriarClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_butaoCriarClienteMouseClicked

        if(nomeCliente.getText().length() < 3){
            new DialogoMsg("Nome do Cliente precisa ter ao menos 3 caracteres");
            return;
        }
        Pattern pattern = Pattern.compile("\\d+.\\d+");


        try{
            Cliente cliente = new Cliente();
            cliente.setNome(nomeCliente.getText());
            cliente.setFisico(fisicoCheck.isSelected());
            cliente.setPagante(paganteCheck.isSelected());
            if(CreateClienteController.add(cliente)){
                table();
            }else{
                new DialogoMsg("N達o foi possivel Criar o cliente");
                return;
            }
        }catch (Exception e){
            new DialogoMsg("Salario do cliente invalido");
            return;
        }





    }//GEN-LAST:event_butaoCriarClienteMouseClicked

    private void excluirClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_excluirClienteMouseClicked
        try {
            Cliente cliente = GetClienteController.get( (UUID) listaClientes.getValueAt(listaClientes.getSelectedRow(), 0));
            if (cliente != null) {
                if (DeleteClienteController.delete(cliente)) {
                    table();
                } else {
                    new DialogoMsg("N達o foi possivel excluir o cliente!");
                }
            } else {
                new DialogoMsg("Cliente n達o encontrado!");
            }
        }catch (Exception e){
            new DialogoMsg("Nenhum Cliente selecionado!");
        }
    }//GEN-LAST:event_excluirClienteMouseClicked

    private void logarClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_logarClienteMouseClicked
        try {
            Cliente cliente = GetClienteController.get((UUID) listaClientes.getValueAt(listaClientes.getSelectedRow(), 0));
            if (cliente != null) {
                new PageCliente(cliente).setVisible(true);
                this.setVisible(false);

            } else {
                new DialogoMsg("Cliente n達o encontrado!");
            }
        }catch (Exception e){
            new DialogoMsg("Nenhum Cliente selecionado!");
        }
    }//GEN-LAST:event_logarClienteMouseClicked

