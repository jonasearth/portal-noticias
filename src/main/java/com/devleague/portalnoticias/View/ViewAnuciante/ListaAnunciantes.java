    private void table(){

        ArrayList<Anunciante> anunciantes = GetAnuncianteController.getAll();


        ListaAnunciantesTable tm = new ListaAnunciantesTable(anunciantes);


        listaAnunciantes = new JTable(tm);
        listaAnunciantes.getColumnModel().getColumn(0).setPreferredWidth(200);
        jScrollPane1.setViewportView(listaAnunciantes);


    }

