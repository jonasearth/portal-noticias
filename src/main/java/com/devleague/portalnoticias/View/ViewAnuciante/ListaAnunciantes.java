    private void butaoCriarAnuncianteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_butaoCriarAnuncianteMouseClicked

        if(nomeAnunciante.getText().length() < 3){
            new DialogoMsg("Nome do Anunciante precisa ter ao menos 3 caracteres");
            return;
        }
        Pattern pattern = Pattern.compile("\\d+.\\d+");

        if(!pattern.matcher(salarioAnunciante.getText()).matches()){
            new DialogoMsg("Salario do anunciante invalido");
            return;
        }
        try{
            Anunciante anunciante = new Anunciante();
            anunciante.setNome(nomeAnunciante.getText());
            if(CreateAnuncianteController.add(anunciante)){
                table();
            }else{
                new DialogoMsg("Não foi possivel Criar o anunciante");
                return;
            }
        }catch (Exception e){
            new DialogoMsg("Salario do anunciante invalido");
            return;
        }





    }//GEN-LAST:event_butaoCriarAnuncianteMouseClicked

    private void excluirAnuncianteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_excluirAnuncianteMouseClicked
        try {
            Anunciante anunciante = GetAnuncianteController.get( (UUID) listaAnunciantes.getValueAt(listaAnunciantes.getSelectedRow(), 0));
            if (anunciante != null) {
                if (DeleteAnuncianteController.delete(anunciante)) {
                    table();
                } else {
                    new DialogoMsg("Não foi possivel excluir o anunciante!");
                }
            } else {
                new DialogoMsg("Anunciante não encontrado!");
            }
        }catch (Exception e){
            new DialogoMsg("Nenhum Anunciante selecionado!");
        }
    }//GEN-LAST:event_excluirAnuncianteMouseClicked

    private void logarAnuncianteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_logarAnuncianteMouseClicked
        try {
            Anunciante anunciante = GetAnuncianteController.get((UUID) listaAnunciantes.getValueAt(listaAnunciantes.getSelectedRow(), 0));
            if (anunciante != null) {
                new PageAnunciante(anunciante).setVisible(true);
                this.setVisible(false);

            } else {
                new DialogoMsg("Anunciante não encontrado!");
            }
        }catch (Exception e){
            new DialogoMsg("Nenhum Anunciante selecionado!");
        }
    }//GEN-LAST:event_logarAnuncianteMouseClicked

